package com.example.com.jglx.android.app.info;

public class ServerGridItem {
	
    
        private String title; 
        private int imageId; 
        private String description;
        
        public ServerGridItem() 
        { 
            super(); 
        } 
     
        public ServerGridItem(String title, int imageId) 
        { 
            super(); 
            this.title = title; 
            this.imageId = imageId; 
        } 
     
        public String getTime( )
        {
            return description;
        }

        public String getTitle() 
        { 
            return title; 
        } 
     
        public int getImageId() 
        { 
            return imageId; 
        } 
    
}
